def erreur_proportionelle(x: float, y: float) -> float:
    if x == y == 0:
        return 0
    elif x == 0 or y == 0:
        return 10**100
    return abs(max(x, y)/min(x, y) - 1)

def trouver_intervalle_proche_zero(f: callable, taylor_x0: Polynomial, x0: float, delta: float, marge_erreur: float, min_delta: float, max_delta: float):
    def point_valide(x):
        return erreur_proportionelle(f(x), taylor_x0(x)) <= marge_erreur

    if not point_valide(x0):
        print(f'{float(f(x0))=}, {float(taylor_x0(x0))=}, {x0=}, {delta=}, {marge_erreur=}')
        raise ValueError('no delta could be found as a delat of 0 did not satisfy condition')
    else:
        delta_a = delta_b = delta
        for mul_factor in (2, 2**(1/4), 2**(1/16), 2**(1/64)):
            if not point_valide(x0 - delta_a):
                while not point_valide(x0 - delta_a):
                    delta_a /= mul_factor
                    if delta_a < min_delta:  # pour éviter une boucle infinie, dans un cas comme sin(1/x)
                        return x0, x0
            else:
                while point_valide(x0 - delta_a):
                    delta_a *= mul_factor
                    if delta_a > max_delta:
                        return x0 - delta_a, x0 + delta_b
                delta_a /= mul_factor

            if not point_valide(x0 + delta_b):
                while not point_valide(x0 + delta_b):
                    delta_b /= mul_factor
                    if delta_b < min_delta:  # pour éviter une boucle infinie, dans un cas comme sin(1/x)
                        return x0, x0
            else:
                while point_valide(x0 + delta_b):
                    delta_b *= mul_factor
                    if delta_b > max_delta:
                        return x0 - delta_a, x0 + delta_b
                delta_b /= mul_factor

        return x0 - delta_a, x0 + delta_b

