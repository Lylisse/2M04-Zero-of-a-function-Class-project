def taylor(f: callable, order: int, x0, h=10 ** -5) -> Polynomial:
    mp.mp.prec = order * -mp.log(h, 2) + 10

    polynomial = Polynomial([mp.mpf(0)])

    h = mp.mpf(h)
    x0 = mp.mpf(x0)

    denominateur = 1

    f_as_dict = {}

    x_moins_a_puissance_n = Polynomial([mp.mpf(1)])

    for n in range(-order + 1, order):
        f_as_dict[n] = f(x0 + n * h)

    for n in range(order):
        derivee_n = derivatives_up_to_100[n]

        total = 0
        for coeff_h, coeff_f in derivee_n.items():
            total += mp.mpf(coeff_f) * f_as_dict[mp.mpf(coeff_h)]

        polynomial += x_moins_a_puissance_n * total / denominateur
        x_moins_a_puissance_n *= Polynomial([-x0, 1])

        denominateur *= (n + 1) * 2 * h

    float_poly = Polynomial([0.0])
    for exp, coef in enumerate(polynomial.coef):
        float_poly += Polynomial([0.0] * exp + [float(coef)])

